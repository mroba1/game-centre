async function main() {
  for (const c of CATEGORIES) {
    await prisma.category.upsert({
      where: { name: c.name },
      update: {},
      create: c,
    });
  }

  for (const s of SETTINGS) {
    // Never overwrite `value` on an existing row — admin-configured settings
    // (bank details, fee %, stake tiers, ...) must survive a reseed/redeploy.
    // Only brand-new keys get their seed default value.
    await prisma.setting.upsert({
      where: { key: s.key },
      update: { description: s.description },
      create: s,
    });
  }

  for (const q of QUESTIONS) {
    const category = await prisma.category.findUniqueOrThrow({ where: { name: q.category } });
    const existing = await prisma.question.findFirst({ where: { prompt: q.prompt } });
    if (existing) continue;

    await prisma.question.create({
      data: {
        categoryId: category.id,
        topic: q.topic,
        difficulty: q.difficulty,
        prompt: q.prompt,
        explanation: q.explanation,
        tags: q.tags,
        options: {
          create: q.options.map((o, i) => ({
            label: o.label,
            isCorrect: o.isCorrect,
            sortOrder: i,
          })),
        },
      },
    });
  }

  const adminEmail = process.env.SEED_ADMIN_EMAIL ?? "admin@wavworkshop.local";
  const adminPassword = process.env.SEED_ADMIN_PASSWORD ?? "ChangeMe123!";
  const existingAdmin = await prisma.user.findUnique({ where: { email: adminEmail } });
  if (!existingAdmin) {
    const admin = await prisma.user.create({
      data: {
        email: adminEmail,
        username: "admin",
        passwordHash: await hash(adminPassword),
        role: Role.ADMIN,
        emailVerified: new Date(),
        adminProfile: { create: {} },
        wallet: { create: {} },
      },
    });
    console.log(`Seeded admin user: ${admin.email} / (password set via SEED_ADMIN_PASSWORD)`);
  }

  console.log("Seed complete.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
